// Se definen las variables para el área y el perímetro
let area;
let perimetro;

// Se definen las dimensiones del rectángulo como constantes
const base = 25;
const altura = 35;

// Se calculan el área y el perímetro del rectángulo
area = base * altura;
perimetro = 2 * (base + altura);

// Se muestra el resultado en la consola
console.log("El resultado es: " + area + " y " + perimetro);